# TypeCasting : Converting one data type to another data type

#? Converting string to integer
age = "20"
print(type(age))

age = int(age)
print(type(age))

age += 5
print("Age after 5 years will be:", age)


#?  Converting string to float
price = "19.99"
print(type(price))

price = float(price)
print(type(price))

price += 5.0
print("Price after discount will be:", price)


#? Converting integer to float
quantity = 10
print(type(quantity))

quantity = float(quantity)
print(type(quantity))

quantity += 2.5
print("Total quantity is:", quantity)